package JavaCourse.IntroductionOfJava;

public class BasicSyntax{
    // public class BasicSyntaxOfJava { is a class definition
    // BasicSyntaxOfJava is a class name
    // public is an access modifier
    // class is a keyword
    // { } is a block
    public static void main(String[] args) {
        System.out.println("Hello Everyone");
        // System.out.println is a method
        // System is a class
        // . (dot) is a member access operator
        // out is a static member of System class
        // println is a method of PrintStream class
        // "Hello Everyone" is a string literal
        // ; is a statement terminator
        // () is a method call operator
        // "" is a double quote
        // System.out.println("Hello Everyone"); is a statement

        // public static void main(String[] args) { is a method definition

        // Access modifier - public
        // Static
        // Return type - void (no return type)
        // Method name - main
        // Parameter - String[] args
    }
}
