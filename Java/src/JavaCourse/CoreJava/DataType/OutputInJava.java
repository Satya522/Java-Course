package src.JavaCourse.CoreJava.JavaBasic.DataType;

public class OutputInJava {
    public static void main(String[] args) {

        // print() output ko print karta hai lekin iske bad koi new line nahi ayegi
        System.out.print("Hello World!");
        // yeh output ko print karta hai console
        System.out.print("Hello World!");
        // output: Hello World!Hello World!  // dono print statement ke output ko ek sath print karega

       // println() output ko print karta hai lekin iske sath hi sath ek new line bhi ayega

        System.out.println("Hello World!");
        // yeh output print karega new line ke sath
        System.out.print("Hello World!");
        // output: Hello World!
        //         Hello World!  // dono print statement ke output ko ek sath print karega

    }
}
