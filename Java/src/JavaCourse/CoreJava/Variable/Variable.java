package src.JavaCourse.CoreJava.JavaBasic.Variable;

public class Variable {
    public static void main(String[] args) {
        // Variable
        int myNum = 5;  // myNum is a variable name and 5 is a variable value which is called literal in java.
        System.out.println(myNum);

        double myDouble2 = 9.78; // myDouble is variable name
        System.out.println(myDouble2);


        // String is data type
        String greeting = "Hello";  // greeting is a variable
        System.out.println(greeting);

        // Concatenation is method to add two string data type value
        String firstName = "John";
        String lastName = "Doe";
        System.out.println(firstName + " " + lastName);

        // Adding Numbers and Strings
        String firstName2 = "John";
        String lastName2 = "Doe";
        System.out.println(firstName2 + " " + lastName2);

    }
}
