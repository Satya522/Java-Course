package src.JavaCourse.CoreJava.JavaBasic.LoopControl;

public class whileLoop {
    public static void main(String[] args) {
        // initialization
        int i = 0;
        // termination
        while (i < 10) {
            // statement(s)
            System.out.println("Hello World");
            // increment
            i++;
        }
    }
}
