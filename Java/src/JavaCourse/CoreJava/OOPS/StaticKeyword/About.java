package src.JavaCourse.CoreJava.OOPS.StaticKeyword;
// What is Static Keyword in Java
/*
**The Static keyword in Java is a non-access modifier which is used for methods and attributes.
**Static methods/Attributes can be accessed without creating an object of the class.
**Static methods/Attributes are declared with the static keyword.
**A static method can access static data member and can change the value of it.

 * Using "Static" Keyword
 * 1. Static data member [declared at the class level]
 * 2. Static method
 * 3. Static block
 * 4. Static class [can we used only within the nested class or inner class and not used with the outer class]
  */
public class About {
    public static void main(String[] args) {
        System.out.println("All about Static Keyword ");
    }
}
